                    <p>

                      3/12日は曇りで風もあり頂上付近は雲の流れが速く山頂目指しスタートした。スキー班と一緒に登り始めたが私たちは奥の沢を快適に交代しながら上り詰める途中から強風にあおられながら稜線に出ると予想以上に強風で体が飛ばされそうだ。しかし、もう少し上まで頑張れるとクラスト斜面を登るもEV1,225m地点でホワイトアウト状態で限界と判断し安全な場所でアイゼンに取替下山を開始する。EV1,071m地点まで下ると天候も回復し沢の中でアイゼン歩行・ピッケル操作・耐風姿勢等を行い駐車場まで全員が無事下山した。予定時間より早かったが五色温泉別館に到着し講習会･夕食と1日が終わった。<br>	<br>

                      3/13日は予定より早めにチセヌプリ駐車場に移動しシャクナゲ岳目指した。 昨日の天候が嘘の様に無風で日差しも強く暖かい。順調にスキー場を通過しビーナスの丘を通りシャクナゲ岳の分岐でスキー班と合流し、アイゼンに履き替えシャクナゲ岳の山頂に到着。360°ニセコの山々が美しい中、スキー班と皆で記念写真を写し、下山を開始する。スキー班は各々快適に滑走していった。私達はゆっくりとしたペースでチセの分岐を通過しEV850m付近で全員が揃いシャクナゲ岳をバックに記念写真を写し、下山した。13時には全員が駐車場で閉会を小野会長始め各リーダーの挨拶後に細木さんの三本締めで締めた。コロナ過ではあったが万全の対策で普及委員会の行事を終えた。安全登山が出来、ありがとう。
                      

